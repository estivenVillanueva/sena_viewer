package edu.misena.senaviewer.model;

public class Report {
    public int totalMoviesViewed;
    public int totalSeriesViewed;
    public int totalBooksRead;
    public int totalMagazinesRead;

    public int getTotalMoviesViewed() {
        return totalMoviesViewed;
    }

    public void setTotalMoviesViewed(int totalMoviesViewed) {
        this.totalMoviesViewed = totalMoviesViewed;
    }

    public int getTotalSeriesViewed() {
        return totalSeriesViewed;
    }

    public void setTotalSeriesViewed(int totalSeriesViewed) {
        this.totalSeriesViewed = totalSeriesViewed;
    }

    public int getTotalBooksRead() {
        return totalBooksRead;
    }

    public void setTotalBooksRead(int totalBooksRead) {
        this.totalBooksRead = totalBooksRead;
    }

    public int getTotalMagazinesRead() {
        return totalMagazinesRead;
    }

    public void setTotalMagazinesRead(int totalMagazinesRead) {
        this.totalMagazinesRead = totalMagazinesRead;
    }
}
